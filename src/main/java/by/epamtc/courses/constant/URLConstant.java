package by.epamtc.courses.constant;

/**
 * Class containing constants from URL
 *
 * @author DEA
 */
public final class URLConstant {

    /**
     * Field containing symbol which separate url and request's parameters
     */
    public static final String START_PARAMETERS = "?";

    /**
     * Field containing symbol which separate key and value of parameter
     */
    public static final String KEY_VALUE_SEPARATOR = "=";

    /**
     * Field containing symbol which separate request's parameters
     */
    public static final String PARAMETERS_SEPARATOR = "&";

    /**
     * Private constructor to forbid creation of objects
     */
    private URLConstant() {
    }
}
